export const getLayerColor = (layerNumber: number): string => {
  const layer = CAPABILITY_LAYERS.find(l => l.id === layerNumber);
  return layer?.color || 'bg-gray-500';
};

export const getLayerTextColor = (layerNumber: number): string => {
  const layer = CAPABILITY_LAYERS.find(l => l.id === layerNumber);
  return layer?.textColor || 'text-gray-600';
};

export const getServicesByLayer = (layerNumber: number): PRService[] => {
  return PR_SERVICES.filter(s => s.layerNumber === layerNumber);
};

export const getServiceById = (id: string): PRService | undefined => {
  return PR_SERVICES.find(s => s.id === id);
};
