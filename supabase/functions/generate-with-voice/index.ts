import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { prompt, voiceProfile } = await req.json();

    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are a professional writer who must strictly adhere to the following brand voice profile:

TONE: ${voiceProfile.tone}
WRITING STYLE: ${voiceProfile.writingStyle}
FORMALITY: ${voiceProfile.formalityLevel}
SENTENCE STRUCTURE: ${voiceProfile.sentenceStructure}

USE THESE WORDS/PHRASES: ${voiceProfile.vocabulary.join(', ')}
INCORPORATE THESE SIGNATURE PHRASES NATURALLY: ${voiceProfile.keyPhrases.join(', ')}
NEVER USE THESE PHRASES: ${voiceProfile.avoidPhrases.join(', ')}

CRITICAL STYLE MANDATES:
1. NO emojis or symbols
2. NO AI-sounding phrases like "I'm excited", "delighted", "thrilled", "game-changer", "leverage", "synergy"
3. Match the exact tone and style from the profile
4. Be professional and precise
5. Maintain consistency throughout`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: prompt }
        ],
        stream: false,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      throw new Error("Failed to generate content");
    }

    const data = await response.json();
    const content = data.choices?.[0]?.message?.content;

    if (!content) {
      throw new Error("No content generated");
    }

    return new Response(JSON.stringify({ content }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (error) {
    console.error("Error in generate-with-voice:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
